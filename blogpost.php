                                <a href="#" class="post-title">Heart Pop-corn!</a>
                                <div class="meta-data">by <a href="#">Theodora Altman</a>
                                </div>
                                <p>I love creating fun and festive food with my boys especially for different holidays throughout the year. Every year for Valentine’s Day, I try to make heart-shaped pizza and brownies for example. Basically, when it comes to this season of love you can’t go wrong with any food, desserts or treats that include red, pink and festive heart shapes as far as my boys are concerned. With that in mind, I just know the boys would love this easy recipe for Cupid Popcorn that I got from my friend</p>
								<p>CUPID POPCORN FOR VALENTINE’S DAY:</p>
								<p>Prep Time:
20 mins</p>
<p>Cook Time
10 mins</p>
<p>Setting Time:
1 hr</p>
<p>Total Time:
1 hr 30 mins</p>
								<p>Course: Dessert |
								Cuisine: Dessert |
								Keyword: Valentine’s Day |
								Servings: 4 |
								Calories: 461kcal |</p>
								<p>Author: Theodora Altman</p>
								<p>INGREDIENTS:
1 3.2 oz bag of microwave popcorn
1/2 cup white chocolate chips
1/2 cup conversation hearts
1/4 cup Valentine’s M&Ms
Pink food coloring for candy</p>
								<p>INSTRUCTIONS
Pop the popcorn in the microwave and remove any unpopped kernels by making a very small opening in the top of the bag, holding it upside down, and shaking until the unpopped kernels fall out.
Add popcorn to a bowl and melt chocolate in a microwave-safe container for 60 seconds, stirring after 30 seconds, and then stirring after 10 seconds until completely melted. Be careful not to overheat and seize the chocolate.
Add food color to the white chocolate until it reaches your desired shade of pink and pour over the popcorn while it's still warm. Gently fold the chocolate into the popcorn until it's evenly coated, being careful not to break popcorn.
Spread popcorn in an even layer on a piece of parchment paper, sprinkle with conversation hearts and M&Ms and allow to set for at least one hour.
Separate if necessary and store in an airtight container or bag it up and give it to friends and neighbors!</p>
